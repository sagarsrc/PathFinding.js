function buildGrid(walls) {
    var matrix = [];
    for (var r = 0; r < 15; r++) {
        matrix.push(new Array(15).fill(0));
    }
    walls.forEach(function(w) {
        matrix[w[1]][w[0]] = 1;
    });
    return new PF.Grid(matrix);
}

function countNodesExplored(grid) {
    var count = 0;
    for (var y = 0; y < grid.height; y++) {
        for (var x = 0; x < grid.width; x++) {
            if (grid.nodes[y][x].closed) count++;
        }
    }
    return count;
}

function benchmarkMap(label, walls, startX, startY, endX, endY) {
    var finder = new PF.AStarFinder();
    var baseGrid = buildGrid(walls);

    // Single run to get path and node count (deterministic)
    var singleGrid = baseGrid.clone();
    var path = finder.findPath(startX, startY, endX, endY, singleGrid);
    var nodesExplored = countNodesExplored(singleGrid);
    var pathLength = path.length;

    // Timing: average over RUNS runs
    var start = process.hrtime.bigint();
    for (var i = 0; i < RUNS; i++) {
        var g = baseGrid.clone();
        new PF.AStarFinder().findPath(startX, startY, endX, endY, g);
    }
    var end = process.hrtime.bigint();
    var avgNs = Number(end - start) / RUNS;
    var avgMs = avgNs / 1e6;

    return {
        label: label,
        pathLength: pathLength,
        nodesExplored: nodesExplored,
        avgMs: avgMs.toFixed(4)
    };
}

